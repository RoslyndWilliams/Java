/**
* This program creates a basic loop using the "for" keywords and a variable named "i"
*/

package iForLoop;

public class iLoop
{
    public static void main(String[] args)
    {
        System.out.println("Hello and welcome!");

        for (int i = 1; i <= 5; i++)
        {
            System.out.println("i = " + i);
        }
    }
}
